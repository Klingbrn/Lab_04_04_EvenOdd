public class OddOrEven {
    public static void main(String[] args) {
        int numToExamine = 2;

        if (numToExamine % 2 == 0)
            System.out.println(numToExamine + " is Even");
        else
            System.out.println(numToExamine + " is Odd");
        }
     }

